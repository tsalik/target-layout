package com.tsalik.targetlayout.demo;


public class ProgrammerLevel {

    private final int level;
    private final String title;

    public ProgrammerLevel(int level, String title) {
        this.level = level;
        this.title = title;
    }

    public int getLevel() {
        return level;
    }

    public String getTitle() {
        return title;
    }

}
